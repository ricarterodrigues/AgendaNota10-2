    public static void atualizarTabela(){
        List<Agenda> agendas;
        jTable1.removeAll();
        try {
            agendas = dao.list(userLogado.getEmail());
            String[] cabecalho = {"Nome"};
            String[][] matrizAgendas = new String[agendas.size()][1];
            for (int i = 0; i < agendas.size(); i++) {
                Agenda agenda = agendas.get(i);
                matrizAgendas[i][0] = agenda.getNome();

            }
            System.out.println(matrizAgendas);
            jTable1.removeAll();
            DefaultTableModel modelo = new DefaultTableModel(matrizAgendas, cabecalho);
            jTable1.setModel(modelo);

        } catch (ClassNotFoundException | IOException | SQLException ex) {
            JOptionPane.showMessageDialog(jTable1.getRootPane().getContentPane(), "Falha na conexÃ£o");
        }
    }

